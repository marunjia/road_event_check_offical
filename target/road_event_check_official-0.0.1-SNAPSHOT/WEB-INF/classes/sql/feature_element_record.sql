CREATE TABLE `feature_element_record` (
      `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键ID',
      `tbl_id` bigint(20) NOT NULL COMMENT '告警记录唯一标识',
      `alarm_id` varchar(255) DEFAULT NULL,
      `image_path` varchar(255) DEFAULT NULL COMMENT '图片路径',
      `video_path` varchar(255) DEFAULT NULL COMMENT '视频路径',
      `abnormal_location` varchar(64) DEFAULT NULL COMMENT '异常位置，例如：第1车道、应急车道、硬路肩、道路外',
      `abnormal_type` varchar(64) DEFAULT NULL COMMENT '异常类型，例如：停驶、行人、抛洒物等',
      `involved_vehicle_info` text COMMENT '涉事车辆类型及数量，JSON结构表示，例如：{"小轿车":2,"货车":1}',
      `involved_person_info` text COMMENT '涉事人员类型及数量，JSON结构表示，例如：{"行人":1,"施工人":2}',
      `weather_condition` varchar(16) DEFAULT NULL COMMENT '天气状况：晴、雨、雪',
      `lane_occupy_info` text COMMENT '占道情况，包含交集车道数量及通行状态，JSON结构，例如：{"交集车道数":2,"通行情况":["缓行","阻塞"]}',
      `rescue_force` text COMMENT '施救力量，画面中出现的工程救援车辆和施工人员，JSON结构表示',
      `congestion_status` int(11) DEFAULT NULL COMMENT '0:未发生拥堵，1:发生拥堵',
      `danger_element` varchar(64) DEFAULT NULL COMMENT '危险要素，例如：冒烟、起火、无',
      `disposal_advice` int(11) DEFAULT NULL COMMENT '处置建议：0-无法判断、1-疑似误报、2-尽快确认、3-无需处理',
      `advice_reason` varchar(255) DEFAULT NULL COMMENT '处置建议依据',
      `alarm_element` varchar(255) DEFAULT NULL COMMENT '告警物体',
      `alarm_element_range` varchar(255) DEFAULT NULL COMMENT '告警物附近物体',
      `collection_match_status` int(11) DEFAULT NULL,
      `person_check_flag` int(11) DEFAULT '1' COMMENT '人工检验标签:1未核查，2确认事件，3无需处理，4误报',
      `match_check_flag` int(11) DEFAULT '0' COMMENT '告警关联是否关联：0-未核查，1-是，2-否',
      `match_check_reason` varchar(256) DEFAULT NULL COMMENT '告警关联错误原原因',
      `match_collection_id` int(11) DEFAULT NULL COMMENT '告警关联告警集id',
      `create_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间',
      `modify_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '记录修改时间',
      PRIMARY KEY (`id`),
      UNIQUE KEY `uniq_alarm_image_video` (`alarm_id`,`image_path`,`video_path`),
      KEY `idx_tbl_id` (`tbl_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='特征要素记录表'