CREATE TABLE `algorithm_check_process` (
   `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
   `alarm_id` varchar(255) DEFAULT NULL COMMENT '关联告警记录id',
   `image_id` varchar(64) NOT NULL COMMENT '图像 ID',
   `image_path` varchar(255) DEFAULT NULL COMMENT '图片路径',
   `video_path` varchar(255) DEFAULT NULL COMMENT '视频路径',
   `received_time` datetime NOT NULL COMMENT '接收时间',
   `status` tinyint(4) DEFAULT '0' COMMENT '图像状态',
   `completed_time` datetime DEFAULT NULL COMMENT '检测完成时间',
   `type` varchar(32) DEFAULT NULL COMMENT '检测类型',
   `name` varchar(32) DEFAULT NULL COMMENT '目标名称',
   `score` decimal(6,4) DEFAULT NULL COMMENT '置信度分数',
   `iou` decimal(6,4) DEFAULT NULL COMMENT 'iou',
   `point1_x` int(11) DEFAULT NULL COMMENT '第一个点的 x 坐标',
   `point1_y` int(11) DEFAULT NULL COMMENT '第一个点的 y 坐标',
   `point2_x` int(11) DEFAULT NULL COMMENT '第二个点的 x 坐标',
   `point2_y` int(11) DEFAULT NULL COMMENT '第二个点的 y 坐标',
   `create_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
   `modify_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
   PRIMARY KEY (`id`),
   KEY `idx_alarm_id` (`alarm_id`),
   KEY `idx_image_id` (`image_id`),
   KEY `idx_alarm_image_video` (`alarm_id`,`image_path`,`video_path`)
) ENGINE=InnoDB AUTO_INCREMENT=2161189 DEFAULT CHARSET=utf8mb4 COMMENT='图像检测数据简化表'