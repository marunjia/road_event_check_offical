CREATE TABLE `collection_group_record` (
   `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键ID',
   `collection_id` varchar(255) NOT NULL COMMENT '归属告警集id',
   `group_id` varchar(255) NOT NULL COMMENT '分组ID',
   `group_image_url` varchar(255) DEFAULT NULL COMMENT '分组图片',
   `group_item_type` varchar(255) DEFAULT NULL COMMENT '分组物品类型',
   `tbl_id` bigint(20) DEFAULT NULL COMMENT '告警记录唯一标识',
   `alarm_id` varchar(255) DEFAULT NULL COMMENT '告警ID',
   `image_path` varchar(255) DEFAULT NULL COMMENT '图片路径',
   `video_path` varchar(255) DEFAULT NULL COMMENT '视频路径',
   `event_type` varchar(255) DEFAULT NULL COMMENT '告警类型',
   `alarm_time` datetime DEFAULT NULL COMMENT '告警时间',
   `create_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间',
   `modify_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '记录修改时间',
   PRIMARY KEY (`id`),
   KEY `idx_collection_group` (`collection_id`,`group_id`),
   KEY `idx_tbl_id` (`tbl_id`),
   KEY `idx_item_type` (`group_item_type`),
   KEY `idx_alarm_image_video` (`alarm_id`,`image_path`,`video_path`)
) ENGINE=InnoDB AUTO_INCREMENT=741 DEFAULT CHARSET=utf8mb4 COMMENT='告警集分组记录表'