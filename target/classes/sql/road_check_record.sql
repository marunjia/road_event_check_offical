CREATE TABLE `road_check_record` (
 `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键ID',
 `tbl_id` bigint(20) NOT NULL COMMENT '告警记录唯一标识',
 `alarm_id` varchar(64) NOT NULL COMMENT '告警记录ID',
 `image_path` varchar(255) DEFAULT NULL COMMENT '图片路径',
 `video_path` varchar(255) DEFAULT NULL COMMENT '视频路径',
 `extract_image_url` varchar(255) DEFAULT NULL COMMENT '提取图片URL',
 `image_id` varchar(64) DEFAULT NULL COMMENT '图片ID',
 `status` tinyint(4) DEFAULT '0' COMMENT '检测状态（0：未处理，1：已处理等）',
 `type` varchar(50) DEFAULT NULL COMMENT '检测类型（如 road、lane 等）',
 `name` varchar(100) DEFAULT NULL COMMENT '检测对象名称',
 `original_points` json DEFAULT NULL COMMENT '检测区域的原始坐标点数组（JSON 格式）',
 `scaled_up_points` json DEFAULT NULL COMMENT '检测区域的放大坐标点数组（JSON 格式）',
 `percent` decimal(10,4) DEFAULT NULL COMMENT '重叠像素数占比',
 `road_check_flag` tinyint(4) DEFAULT NULL COMMENT '路面检测结果标签（1、路面内；2、路面外）',
 `extract_point1_x` int(11) DEFAULT NULL COMMENT '提框坐标X1',
 `extract_point1_y` int(11) DEFAULT NULL COMMENT '提框坐标Y1',
 `extract_point2_x` int(11) DEFAULT NULL COMMENT '提框坐标X2',
 `extract_point2_y` int(11) DEFAULT NULL COMMENT '提框坐标Y2',
 `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间',
 `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '记录更新时间',
 PRIMARY KEY (`id`),
 KEY `idx_tbl_id` (`tbl_id`),
 KEY `idx_alarm_image_video` (`alarm_id`,`image_path`,`video_path`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='路面检测记录表'
